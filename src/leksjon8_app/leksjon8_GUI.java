    private void btnslettActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnslettActionPerformed
        // Slette valgt person
        Person p = (Person) liste.getSelectedValue();
        int index = liste.getSelectedIndex();

        boolean slettet = databasen.slettPerson(p);
        if (slettet) {
            listeinnhold.remove(index);
            if (listeinnhold.size() > 0) {
                liste.setSelectedIndex(0);
            } else {
                btnEndre.setEnabled(false);
            }
        } else {
            JOptionPane.showMessageDialog(rootPane, "Får ikke slettet personen, kan være slettet av andre");
        }
    }//GEN-LAST:event_btnslettActionPerformed

    private void btnNyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNyActionPerformed
        /* Registrerer to personer - en på "gammel-måten" vha JOptionPane,
         * en vha av et dialogvindu
        */
        //Registrer ny person
        String fornavn = JOptionPane.showInputDialog(rootPane, "Endre fornavn:");
        String etternavn = JOptionPane.showInputDialog(rootPane, "Endre etternavn:");
        Person p = new Person(-1, fornavn.toUpperCase(), etternavn.toUpperCase());
        if (databasen.regPerson(p)) {
            listeinnhold.addElement(p);
            JOptionPane.showMessageDialog(rootPane, "Person registrert: " + p);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Noe gikk galt under registrering");
        }
        if (listeinnhold.size() == 0) {
            btnEndre.setEnabled(true);
        }

        // Bruker en dialog istedet;
        persondialog pd = new persondialog(this, true);
        p = pd.showDialog();
        if (databasen.regPerson(p)) {
            listeinnhold.addElement(p);
            JOptionPane.showMessageDialog(rootPane, "Person registrert: " + p);
        } else {
            JOptionPane.showMessageDialog(rootPane, "Noe gikk galt under registrert: ");
        }
        if (listeinnhold.size() == 0) {
            btnEndre.setEnabled(true);
        }


    }//GEN-LAST:event_btnNyActionPerformed

    private void btnEndreActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEndreActionPerformed
        // Endrer et navn
        Person p = (Person) liste.getSelectedValue();
        int index = liste.getSelectedIndex();
        String fornavn = JOptionPane.showInputDialog(rootPane, "Endre fornavn", p.getFornavn());
        String etternavn = JOptionPane.showInputDialog(rootPane, "Endre etternavn", p.getEtternavn());
        p.setFornavn(fornavn.toUpperCase());
        p.setEtternavn(etternavn.toUpperCase());
        if (databasen.updatePerson(p)) {
            listeinnhold.set(index, p);
            liste.setSelectedIndex(0);
        }
    }//GEN-LAST:event_btnEndreActionPerformed

