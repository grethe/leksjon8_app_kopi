    private void fyllListeMedData() {
        listeinnhold.clear();
        ArrayList<Person> alle = databasen.getAllePersoner();
        if (alle != null) {
            for (Person p : alle) {
                listeinnhold.addElement(p);
            }
            if (listeinnhold.size() > 0) {
                liste.setSelectedIndex(0);
            } else {
                btnEndre.setEnabled(false);
            }
        }
    }

